// Primative Types

public class PrimativeVariableTypes{

    public static void main(String[] args){
        byte age = 23;
        short myAccountBallance = 3_000;
        int youtubeViews = 1_000_000;
        long worldPopulation = 7_808_644_112L;
        float priceOfPen = 10.56F;
        double pi = 3.1415926535;
        char letter = 'Z';
        boolean inMain = true;

        System.out.println("byte... age: " + age);
        System.out.println("short... myAccountBallance: " + myAccountBallance);
        System.out.println("int... youtubeViews: " + youtubeViews);
        System.out.println("long... worldPopulation: " + worldPopulation);
        System.out.println("float... priceOfPen: " + priceOfPen);
        System.out.println("double... pi: " + pi);
        System.out.println("char... letter: " + letter);
        System.out.println("boolean... inMian: " + inMain);

    }
}
